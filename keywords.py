# ──────────────────────────────────────────
# 工具函数：统计关键词命中
# ──────────────────────────────────────────

def count_keywords(text: str, word_list: list[str]) -> tuple[int, list[str]]:
    """
    统计 text 中 word_list 里有多少词被命中。
    返回 (命中数, 命中词列表)
    """
    if not text:
        return 0, []
    hit = [w for w in word_list if w in text]
    return len(hit), hit
